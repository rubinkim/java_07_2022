package com.ruby.java.ch06;
// 실행파일인 Armor.class는 java/bin/com/ruby/java/ch06안에 Armor.class로
// 저장되어 있다.
public class Armor {
	private String name;
	private int height;

	public String getName() {
		return name;
	}
	
	public void setName(String value) {
		name = value;
	}
	
	public int getHeight() {
		return height;
	}
	
	public void setHeight(int value) {
		height = value;
	}
}
